from django.db import models
from django.contrib.auth.models import User
import datetime
import os
 
def getFileName(requset,filename):
  now_time=datetime.datetime.now().strftime("%Y%m%d%H:%M:%S")
  new_filename="%s%s"%(now_time,filename)
  return os.path.join('uploads/',new_filename)

# PRODUCT CATEGORY LIKE BOOK ELECTRONICS HARDWARE OUTFIT ETC
 
class Catagory(models.Model):
  name=models.CharField(max_length=150,null=False,blank=False)
  image=models.ImageField(upload_to=getFileName,null=True,blank=True)
  description=models.TextField(max_length=500,null=False,blank=False)
  status=models.BooleanField(default=False,help_text="0-show,1-Hidden")
  created_at=models.DateTimeField(auto_now_add=True)
 
  def __str__(self) :
    return self.name
 
#THE PRODUCTS COMES UNDER THE CATEGORY IF THAT PARTICULAR CATEGORY DELETED ALL THE PRODUCTS COMES UNDER IT ALSO DELETED

class Product(models.Model):
  category=models.ForeignKey(Catagory,on_delete=models.CASCADE)
  name=models.CharField(max_length=150,null=False,blank=False)
  vendor=models.CharField(max_length=150,null=False,blank=False)
  product_image=models.ImageField(upload_to=getFileName,null=True,blank=True)
  quantity=models.IntegerField(null=False,blank=False)
  original_price=models.FloatField(null=False,blank=False)
  selling_price=models.FloatField(null=False,blank=False)
  description=models.TextField(max_length=500,null=False,blank=False)
  status=models.BooleanField(default=False,help_text="0-show,1-Hidden")
  trending=models.BooleanField(default=False,help_text="0-default,1-Trending")
  created_at=models.DateTimeField(auto_now_add=True)
 
  def __str__(self) :
    return self.name
 
#STORES THE PRODUCT AND QUANTITY,THE VALUES ARE COMES UNDER THE USER IF THE USER IS DELETED THEN THE CART VALUES STORED TO THE USER ALSO DELETED.

class Cart(models.Model):
  user=models.ForeignKey(User,on_delete=models.CASCADE)
  product=models.ForeignKey(Product,on_delete=models.CASCADE)
  product_qty=models.IntegerField(null=False,blank=False)
  created_at=models.DateTimeField(auto_now_add=True)
 
#  CALCULATES THE CURRENT PRODUCT PRICE BY MULTIPLY WITH NO OF QUANTITY
  @property
  def total_cost(self):
    return self.product_qty*self.product.selling_price
 
 #STORES THE PRODUCT AS FAVORITE ,THE VALUES ARE COMES UNDER THE USER IF THE USER IS DELETED THEN THE FAVORITE PRODUCTS STORED TO THE USER ALSO DELETED.

class Favourite(models.Model):
	user=models.ForeignKey(User,on_delete=models.CASCADE)
	product=models.ForeignKey(Product,on_delete=models.CASCADE)
	created_at=models.DateTimeField(auto_now_add=True)
 